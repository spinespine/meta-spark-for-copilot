export function t(key: string, ...args: (string | number)[]): string {
	const dict = isZh() ? zh : en;
	let template = dict[key] ?? (en as any)[key] ?? key;
	for (let i = 0; i < args.length; i++) {
		template = template.replace(`{${i}}`, String(args[i]));
	}
	return template;
}
